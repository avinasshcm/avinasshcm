package pojo;

public class HistoryData {

    int historyColumnNumber;

    boolean isUpdate;

    public int getHistoryColumnNumber() {
        return historyColumnNumber;
    }

    public void setHistoryColumnNumber(int historyColumnNumber) {
        this.historyColumnNumber = historyColumnNumber;
    }

    public boolean isUpdate() {
        return isUpdate;
    }

    public void setUpdate(boolean isUpdate) {
        this.isUpdate = isUpdate;
    }
}
