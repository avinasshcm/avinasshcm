package dd.perf;

public class AccountInfo {
	String cdtrAcct;
	String dbtrAcc;
	String creditorIdentifier;
	String partyID;

	public String getCdtrAcct() {
		return cdtrAcct;
	}

	public void setCdtrAcct(String cdtrAcct) {
		this.cdtrAcct = cdtrAcct;
	}

	public String getDbtrAcc() {
		return dbtrAcc;
	}

	public void setDbtrAcc(String dbtrAcc) {
		this.dbtrAcc = dbtrAcc;
	}

	public String getCreditorIdentifier() {
		return creditorIdentifier;
	}

	public void setCreditorIdentifier(String creditorIdentifier) {
		this.creditorIdentifier = creditorIdentifier;
	}

	public String getPartyID() {
		return partyID;
	}

	public void setPartyID(String partyID) {
		this.partyID = partyID;
	}
}
